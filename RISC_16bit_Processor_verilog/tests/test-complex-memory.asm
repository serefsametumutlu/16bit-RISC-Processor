addi r2, r0, 20    # r2 = 20
add  r3, r1, r2    # r3 = 50
sw   r3, 0(r0)     # mem[0] = 50
addi r4, r0, 4     # r4 = 4
sw   r2, 0(r4)     # mem[4] = 20
lw   r5, 0(r0)     # r5 = mem[0] = 50
lw   r6, 0(r4)     # r6 = mem[4] = 20
add  r7, r5, r6    # r7 = 70
sw   r7, 8(r0)     # mem[8] = 70



add r2, r1, r1     # R2 = 10+10=20 (Forwarding EX->EX)
add r3, r2, r1     # R3 = 20+10=30 (Forwarding EX->EX & WB->EX)
sw r3, 0(r0)       # Mem[0] = 30
lw r4, 0(r0)       # R4 = 30
add r5, r4, r2     # R5 = 30+20=50 (Load-Use Forwarding)
sub r6, r5, r3     # R6 = 50-30=20

